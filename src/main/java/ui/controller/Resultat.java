package ui.controller;

public class Resultat {
	private final String  name;
	private final int value;
	
	public Resultat(String name, int value) {
		super();
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public int getValue() {
		return value;
	}
	
	
	

	

}
